struct GeneratedEnumCaseDescription<T: Encodable>: Encodable {
    let name: String
    let value: T
    let declaredVersion: String?
    let effectiveVersion: String?

    enum CodingKeys: CodingKey {
        case name
        case value
        case declaredVersion
        case effectiveVersion
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(name, forKey: .name)
        try container.encode(value, forKey: .value)
        try container.encode(declaredVersion, forKey: .declaredVersion)
        try container.encode(effectiveVersion, forKey: .effectiveVersion)
    }
}

struct GeneratedEnumDescription: Encodable {
    let iosTypeName: String?
    let androidTypeName: String?
    let cppTypeName: String?

    let intCases: [GeneratedEnumCaseDescription<Int>]?
    let stringCases: [GeneratedEnumCaseDescription<String>]?
    let declaredVersion: String?
    let effectiveVersion: String?

    enum CodingKeys: CodingKey {
        case iosTypeName
        case androidTypeName
        case cppTypeName
        case intCases
        case stringCases
        case declaredVersion
        case effectiveVersion
    }

    static func from(exportedEnum: ExportedEnum, baseline: String?) -> GeneratedEnumDescription {
        let declaredVersion = exportedEnum.declaredVersion
        let effectiveVersion = declaredVersion ?? baseline

        let intCases: [GeneratedEnumCaseDescription<Int>]?
        let stringCases: [GeneratedEnumCaseDescription<String>]?
        switch exportedEnum.cases {
        case let .enum(cases):
            intCases = cases.map { enumCase in
                return GeneratedEnumCaseDescription(
                    name: enumCase.name,
                    value: enumCase.value,
                    declaredVersion: enumCase.declaredVersion,
                    effectiveVersion: nativeApiEffectiveMemberVersion(
                        declared: enumCase.declaredVersion,
                        container: effectiveVersion
                    )
                )
            }
            stringCases = nil
        case let .stringEnum(cases):
            intCases = nil
            stringCases = cases.map { enumCase in
                return GeneratedEnumCaseDescription(
                    name: enumCase.name,
                    value: enumCase.value,
                    declaredVersion: enumCase.declaredVersion,
                    effectiveVersion: nativeApiEffectiveMemberVersion(
                        declared: enumCase.declaredVersion,
                        container: effectiveVersion
                    )
                )
            }
        }

        return GeneratedEnumDescription(
            iosTypeName: exportedEnum.iosType?.name,
            androidTypeName: exportedEnum.androidTypeName,
            cppTypeName: exportedEnum.cppType?.declaration.fullTypeName,
            intCases: intCases,
            stringCases: stringCases,
            declaredVersion: declaredVersion,
            effectiveVersion: effectiveVersion
        )
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encodeIfPresent(iosTypeName, forKey: .iosTypeName)
        try container.encodeIfPresent(androidTypeName, forKey: .androidTypeName)
        try container.encodeIfPresent(cppTypeName, forKey: .cppTypeName)
        try container.encodeIfPresent(intCases, forKey: .intCases)
        try container.encodeIfPresent(stringCases, forKey: .stringCases)
        try container.encode(declaredVersion, forKey: .declaredVersion)
        try container.encode(effectiveVersion, forKey: .effectiveVersion)
    }
}

struct GeneratedFunctionDescription: Encodable {
    let containingIosTypeName: String?
    let containingAndroidTypeName: String?
    let containingCppTypeName: String?
    let functionName: String
    let parameters: [PropertyDescription]
    let returnType: PropertyTypeDescription
    let declaredVersion: String?
    let effectiveVersion: String?

    enum CodingKeys: CodingKey {
        case containingIosTypeName
        case containingAndroidTypeName
        case containingCppTypeName
        case functionName
        case parameters
        case returnType
        case declaredVersion
        case effectiveVersion
    }

    static func from(exportedFunction: ExportedFunction, baseline: String?) -> GeneratedFunctionDescription {
        let declaredVersion = exportedFunction.declaredVersion
        return GeneratedFunctionDescription(
            containingIosTypeName: exportedFunction.containingIosType?.name,
            containingAndroidTypeName: exportedFunction.containingAndroidTypeName,
            containingCppTypeName: exportedFunction.containingCppType?.declaration.fullTypeName,
            functionName: exportedFunction.functionName,
            parameters: exportedFunction.parameters.map(PropertyDescription.init),
            returnType: PropertyTypeDescription(propType: exportedFunction.returnType),
            declaredVersion: declaredVersion,
            effectiveVersion: declaredVersion ?? baseline
        )
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encodeIfPresent(containingIosTypeName, forKey: .containingIosTypeName)
        try container.encodeIfPresent(containingAndroidTypeName, forKey: .containingAndroidTypeName)
        try container.encodeIfPresent(containingCppTypeName, forKey: .containingCppTypeName)
        try container.encode(functionName, forKey: .functionName)
        try container.encode(parameters, forKey: .parameters)
        try container.encode(returnType, forKey: .returnType)
        try container.encode(declaredVersion, forKey: .declaredVersion)
        try container.encode(effectiveVersion, forKey: .effectiveVersion)
    }
}

