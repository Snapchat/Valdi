import Foundation

func nativeApiDeclaredVersion(annotations: [ValdiTypeScriptAnnotation]) -> String? {
    return annotations.first(where: { $0.name == ValdiAnnotationType.version.rawValue })?.positionalPayload
}

func nativeApiEffectiveMemberVersion(declared: String?, container: String?) -> String? {
    guard let declared else {
        return container
    }
    guard let container else {
        return declared
    }
    if declared == "__PLACEHOLDER__" || container == "__PLACEHOLDER__" {
        return "__PLACEHOLDER__"
    }
    guard let declaredVersion = Int(declared) else {
        return declared
    }
    guard let containerVersion = Int(container) else {
        return container
    }
    return String(max(declaredVersion, containerVersion))
}

