// class for indirection
class PropertyTypeDescription: Encodable {
    let typeStr: String

    private(set) var functionTypeMetadata: FunctionTypeMetadata?
    struct FunctionTypeMetadata: Encodable {
        let parameters: [PropertyDescription]
        let returnType: PropertyTypeDescription
    }

    private(set) var genericTypeMetadata: GenericTypeMetadata?
    struct GenericTypeMetadata: Encodable {
        let types: [PropertyTypeDescription]
    }

    private(set) var customTypeMetadata: CustomTypeMetadata?
    struct CustomTypeMetadata: Encodable {
        let iosTypeName: String?
        let androidClassName: String?
        let cppTypeName: String?
    }

    private(set) var typeParameterMetadata: TypeParameterMetadata?
    struct TypeParameterMetadata: Encodable {
        let typeParameterName: String
    }

    init(propType: ValdiModelPropertyType) {
        switch propType {
        case .string:
            typeStr = "string"
        case .double:
            typeStr = "double"
        case .bool:
            typeStr = "bool"
        case .long:
            typeStr = "long"
        case let .array(elementType):
            typeStr = "array"
            let description = PropertyTypeDescription(propType: elementType)
            genericTypeMetadata = GenericTypeMetadata(types: [description])
        case .bytes:
            typeStr = "bytes"
        case let .map(keyType, valueType):
            typeStr = "map"
            genericTypeMetadata = GenericTypeMetadata(types: [
                PropertyTypeDescription(propType: keyType),
                PropertyTypeDescription(propType: valueType),
            ])
        case .any:
            typeStr = "any"
        case .void:
            typeStr = "void"
        case let .function(parameters, returnType, _, _, _):
            typeStr = "function"
            let parameters = parameters.map(PropertyDescription.init)
            let returnType = PropertyTypeDescription(propType: returnType)
            functionTypeMetadata = FunctionTypeMetadata(parameters: parameters,
                                                        returnType: returnType)
        case let .object(nodeClassMapping):
            typeStr = "object"
            customTypeMetadata = CustomTypeMetadata(nodeClassMapping: nodeClassMapping)
        case let .genericTypeParameter(name):
            typeStr = "genericTypeParameter"
            typeParameterMetadata = .init(typeParameterName: name)
        case let .genericObject(nodeClassMapping, typeArguments):
            typeStr = "genericObject"
            customTypeMetadata = CustomTypeMetadata(nodeClassMapping: nodeClassMapping)
            genericTypeMetadata = GenericTypeMetadata(types: typeArguments.map(PropertyTypeDescription.init))
        case let .enum(nodeClassMapping):
            typeStr = "enum"
            customTypeMetadata = CustomTypeMetadata(nodeClassMapping: nodeClassMapping)
        case .promise(let typeArgument):
            typeStr = "promise"
            genericTypeMetadata = GenericTypeMetadata(types: [PropertyTypeDescription(propType: typeArgument)])
        case let .nullable(innerType):
            typeStr = "nullable"
            genericTypeMetadata = GenericTypeMetadata(types: [PropertyTypeDescription(propType: innerType)])
        }
    }

}

private extension PropertyTypeDescription.CustomTypeMetadata {
    init(nodeClassMapping: ValdiNodeClassMapping) {
        iosTypeName = nodeClassMapping.iosType?.name
        androidClassName = nodeClassMapping.androidClassName
        cppTypeName = nodeClassMapping.cppType?.declaration.fullTypeName
    }
}

struct PropertyDescription: Encodable {
    let name: String
    let isOptional: Bool

    let type: PropertyTypeDescription
    let declaredVersion: String?
    let effectiveVersion: String?
    private let emitsVersionMetadata: Bool

    // not for interfaces
    let omitConstructor: OmitConstructorParams?

    enum CodingKeys: CodingKey {
        case name
        case isOptional
        case type
        case declaredVersion
        case effectiveVersion
        case omitConstructor
    }

    init(prop: ValdiModelProperty) {
        name = prop.name
        isOptional = prop.type.isOptional
        type = PropertyTypeDescription(propType: prop.type.unwrappingOptional)
        omitConstructor = prop.omitConstructor
        declaredVersion = nil
        effectiveVersion = nil
        emitsVersionMetadata = false
    }

    init(prop: ValdiModelProperty, declaredVersion: String?, containerVersion: String?) {
        name = prop.name
        isOptional = prop.type.isOptional
        type = PropertyTypeDescription(propType: prop.type.unwrappingOptional)
        omitConstructor = prop.omitConstructor
        self.declaredVersion = declaredVersion
        effectiveVersion = nativeApiEffectiveMemberVersion(
            declared: declaredVersion,
            container: containerVersion
        )
        emitsVersionMetadata = true
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(name, forKey: .name)
        try container.encode(isOptional, forKey: .isOptional)
        try container.encode(type, forKey: .type)
        if emitsVersionMetadata {
            try container.encode(declaredVersion, forKey: .declaredVersion)
            try container.encode(effectiveVersion, forKey: .effectiveVersion)
        }
        try container.encodeIfPresent(omitConstructor, forKey: .omitConstructor)
    }
}
