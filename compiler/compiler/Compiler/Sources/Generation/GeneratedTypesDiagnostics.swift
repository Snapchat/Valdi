struct GeneratedTypesSummary: Encodable {
    let sourceFilePath: String
    let generatedTypes: [GeneratedTypeDescription]

    func toFile() -> File {
        let data = (try? toJSON(outputFormatting: [.prettyPrinted, .sortedKeys])) ?? Data()
        let file = File.data(data)
        return file
    }
}

enum GeneratedTypeDescription: Encodable {
    case `class`(GeneratedNativeClassDescription)
    case interface(GeneratedNativeInterfaceDescription)
    case `enum`(GeneratedEnumDescription)
    case function(GeneratedFunctionDescription)
    case module(GeneratedNativeInterfaceDescription)
    case viewClass(GeneratedViewClassDescription)

    enum CodingKeys: CodingKey {
        case `class`
        case interface
        case `enum`
        case function
        case module
        case viewClass
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        switch self {
        case let .class(value):
            try container.encode(value, forKey: .class)
        case let .interface(value):
            try container.encode(value, forKey: .interface)
        case let .enum(value):
            try container.encode(value, forKey: .enum)
        case let .function(value):
            try container.encode(value, forKey: .function)
        case let .module(value):
            try container.encode(value, forKey: .module)
        case let .viewClass(value):
            try container.encode(value, forKey: .viewClass)
        }
    }

    func typeName(for platform: Platform) -> String? {
        switch platform {
        case .ios:
            return iosTypeName
        case .android:
            return androidTypeName
        case .web:
            return webTypeName
        case .cpp:
            return cppTypeName
        }
    }

    var iosTypeName: String? {
        switch self {
        case let .class(this):
            return this.iosTypeName
        case let .interface(this):
            return this.iosTypeName
        case let .enum(this):
            return this.iosTypeName
        case let .function(this):
            return this.containingIosTypeName
        case let .module(this):
            return this.iosTypeName
        case let .viewClass(this):
            return this.iosTypeName
        }
    }

    var androidTypeName: String? {
        switch self {
        case let .class(this):
            return this.androidTypeName
        case let .interface(this):
            return this.androidTypeName
        case let .enum(this):
            return this.androidTypeName
        case let .function(this):
            return this.containingAndroidTypeName
        case let .module(this):
            return this.androidTypeName
        case let .viewClass(this):
            return this.androidClassName
        }
    }

    var cppTypeName: String? {
        switch self {
        case let .class(this):
            return this.cppTypeName
        case let .interface(this):
            return this.cppTypeName
        case let .enum(this):
            return this.cppTypeName
        case let .function(this):
            return this.containingCppTypeName
        case let .module(this):
            return this.cppTypeName
        case .viewClass:
            return nil
        }
    }

    var webTypeName: String? {
        // TODO: Implement this
        return nil
    }

    var valueToSortBy: String {
        switch self {
        case let .class(this):
            return this.iosTypeName ?? this.androidTypeName ?? ""
        case let .interface(this):
            return this.iosTypeName ?? this.androidTypeName ?? ""
        case let .enum(this):
            return this.iosTypeName ?? this.androidTypeName ?? ""
        case let .function(this):
            return this.functionName
        case let .module(this):
            return this.iosTypeName ?? this.androidTypeName ?? ""
        case let .viewClass(this):
            return this.iosTypeName ?? this.androidClassName ?? ""
        }
    }

    var isNativeApi: Bool {
        switch self {
        case let .class(description),
             let .interface(description),
             let .module(description):
            return description.isNativeApi
        case .enum, .function:
            return true
        case .viewClass:
            return false
        }
    }
}

struct GeneratedNativeClassDescription: Encodable {
    let iosTypeName: String?
    let androidTypeName: String?
    let cppTypeName: String?
    let properties: [PropertyDescription]
    let declaredVersion: String?
    let effectiveVersion: String?
    let isNativeApi: Bool

    enum CodingKeys: CodingKey {
        case iosTypeName
        case androidTypeName
        case cppTypeName
        case properties
        case declaredVersion
        case effectiveVersion
    }

    init(model: ValdiModel, baseline: String?) {
        iosTypeName = model.iosType?.name
        androidTypeName = model.androidClassName
        cppTypeName = model.cppType?.declaration.fullTypeName
        declaredVersion = model.declaredVersion
        isNativeApi = model.isNativeApi
        let effectiveVersion = model.declaredVersion ?? (model.isNativeApi ? baseline : nil)
        self.effectiveVersion = effectiveVersion
        properties = model.properties.map { property in
            if model.isNativeApi {
                return PropertyDescription(
                    prop: property,
                    declaredVersion: property.declaredVersion,
                    containerVersion: effectiveVersion
                )
            } else {
                return PropertyDescription(prop: property)
            }
        }
    }

    init(nativeClass: TypeScriptNativeClass,
         annotations: [ValdiTypeScriptAnnotation],
         baseline: String?) {
        iosTypeName = nativeClass.iosType?.name
        androidTypeName = nativeClass.androidClass
        cppTypeName = nativeClass.cppType?.declaration.fullTypeName
        properties = []
        declaredVersion = nativeApiDeclaredVersion(annotations: annotations)
        effectiveVersion = declaredVersion ?? baseline
        isNativeApi = true
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encodeIfPresent(iosTypeName, forKey: .iosTypeName)
        try container.encodeIfPresent(androidTypeName, forKey: .androidTypeName)
        try container.encodeIfPresent(cppTypeName, forKey: .cppTypeName)
        try container.encode(properties, forKey: .properties)
        try container.encode(declaredVersion, forKey: .declaredVersion)
        try container.encode(effectiveVersion, forKey: .effectiveVersion)
    }
}

